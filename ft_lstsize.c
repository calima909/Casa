int ft_lstsize(t_list *lst)
{
    int i;
    
    i = 0;

    if (lst == NULL)
        return(0);    
    while (lst->next != NULL)
    {
        i++;
        lst = lst->next;
    }
    if (lst->next == NULL)
        i++;
    return(i);
}
